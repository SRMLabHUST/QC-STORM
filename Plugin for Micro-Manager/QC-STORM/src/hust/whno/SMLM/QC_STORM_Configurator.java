        jComboBox_CtlBoxUART.setSelectedIndex(UartidI);
        

        File Parafile = new File(FilePath);
        File Parafiledir = new File(Parafile.getParent());


        if (Parafiledir.exists())
        {    
        }else
        {
             Parafiledir.mkdirs();
        }

        if (Parafile.exists())
        {
            
        }else
        {
             Parafile.createNewFile();
        }
        

        // load property file
        
        Properties pps = new Properties();

